#include <cstdio>
#include "help.h"

//-----------------------------------------------------------------------------
//! \brief   help
//! \details help accessable from command line
//-----------------------------------------------------------------------------
int help(int argc, char *argv[])
{
  printf("\n-----------------------------------------------------------------------------");
  printf("\n|  Usage:");
  printf("\n|  %s command1 [arg1a] [arg1b] [...] [command2] [arg2a] ...", argv[0]);
  printf("\n-----------------------------------------------------------------------------");
  return 0;
}

